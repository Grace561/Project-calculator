// create the math function
function add(a, b){
    return a + b;
}

function subtract(a, b){
    return a - b;
}

function multiply(a, b){
    return a * b;
}

function divide(a, b){
    return a / b;
}
console.log(add(2, 3));
console.log(subtract(10, 5));
console.log(multiply(4, 6));
console.log(divide(20, 5));